// ── Types ─────────────────────────────────────────────────────────────────────
export type NoticePriority = "Low" | "Normal" | "High" | "Urgent";
export type NoticeStatus   = "Draft" | "Scheduled" | "Published" | "Archived";
export type NoticeCategory =
  | "General" | "Exam" | "Fees" | "Holiday" | "Event" | "Meeting"
  | "Emergency" | "Academic" | "Transport" | "Hostel" | "Library";
export type AudienceType =
  | "All" | "Students" | "Parents" | "Teachers" | "Staff" | "Students & Parents";

// ── Interfaces ────────────────────────────────────────────────────────────────
export interface NoticeAttachment {
  id:   number;
  name: string;
  type: "PDF" | "Image" | "Document" | "Spreadsheet";
  size: string;
}

export interface NoticeStats {
  totalRecipients: number;
  read:            number;
  unread:          number;
  acknowledged:    number;
}

export interface RecipientRecord {
  id:           number;
  name:         string;
  role:         "Student" | "Parent" | "Teacher" | "Staff";
  classSection: string;
  readStatus:   "Read" | "Unread";
  readTime?:    string;
  acknowledged: boolean;
}

export interface ActivityRecord {
  id:     number;
  action: string;
  by:     string;
  at:     string;
  icon:   "create" | "edit" | "publish" | "archive" | "read" | "approve";
}

export interface Notice {
  id:              number;
  noticeId:        string;
  title:           string;
  category:        NoticeCategory;
  priority:        NoticePriority;
  status:          NoticeStatus;
  audience:        AudienceType;
  targetClasses:   string[];
  targetSections:  string[];
  summary:         string;
  body:            string;
  importantInstructions?: string;
  externalLink?:   string;
  publishedBy:     string;
  publishDate:     string;
  publishTime:     string;
  expiryDate?:     string;
  pinned:          boolean;
  sendNotification: boolean;
  sendEmail:       boolean;
  sendSms:         boolean;
  attachments:     NoticeAttachment[];
  stats:           NoticeStats;
  recipients:      RecipientRecord[];
  activities:      ActivityRecord[];
  internalNotes?:  string;
  approvalRemarks?: string;
}

